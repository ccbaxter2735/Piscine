#include <unistd.h>

void	ft_nb(char c)
{
	write(1, &c, 1);
}

void	ft_print_comb2(void)
{
	int	a;
	int	b;

	a = 0;
	b = 0;
	while (a <= 99)
	{
		while (b <= 99)
		{
			if ((a != b) && (a < b))
			{
				ft_nb('0' + (a / 10));
				ft_nb('0' + (a % 10));
				ft_nb(' ');
				ft_nb('0' + (b / 10));
				ft_nb('0' + (b % 10));
				if (!(a == 98 && b == 99))
					write(1, ", ", 2);
			}
			b = b + 1;
		}
		b = 0;
		a = a + 1;
	}
}
