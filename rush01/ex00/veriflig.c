int	veriflig(char *tm)
{
	int	i;
	int	ret;

	i = 7;
	ret = 1;
	while (tm[i] != '\0' && i < 26)
	{
		if (tm[i] + tm [i + 1] + tm[i + 2] + tm[i + 3] != 10)
		{
			ret = 0;
			return (ret);
		}
		i += 6;
	}
	return (ret);
}
