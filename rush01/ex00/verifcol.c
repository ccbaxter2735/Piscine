int	verifcol(char *tm)
{
	int	i;
	int	ret;

	ret = 1;
	i = 7;
	while (tm[i] != '\0' && i < 11)
	{
		if (tm[i] + tm[i + 6] + tm[i + 12] + tm[i + 18] != 10)
		{
			ret = 0;
			return (ret);
		}
		i++;
	}
	return (ret);
}
