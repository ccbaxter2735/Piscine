#include <unistd.h>

int	ft_atoi(char *str)
{
	int	i;
	int	signe;
	int	total;

	i = 0;
	signe = 1;
	total = 0;
	while (str[i] == ' ' || (str[i] > 7 && str[i] < 14))
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			signe = -signe;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		total = total * 10 + (str[i] - '0');
		i++;
	}
	return (signe * total);
}
