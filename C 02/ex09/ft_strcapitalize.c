char	*ft_strlowcase(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] = str[i] + 32;
		i++;
	}
	return (str);
}

char	*ft_strcapitalize(char *str)
{
	int	i;
	int	prem;

	i = 0;
	prem = 1;
	ft_strlowcase(str);
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
		{
			if (prem == 1)
				str[i] = str[i] - 32;
			prem = 0;
		}
		else if (str[i] >= '0' && str[i] <= '9')
			prem = 0;
		else
			prem = 1;
		i++;
	}
	return (str);
}
