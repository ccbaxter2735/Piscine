void	ft_rev_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	temp;

	i = 0;
	j = size - 1;
	while (i <= size / 2 - 1)
	{
		temp = tab[i];
		tab[i] = tab[j];
		tab[j] = temp;
		i = i + 1;
		j = j - 1;
	}
}
